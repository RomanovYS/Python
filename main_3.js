// Нужно реализовать функционал подсчета стоимости корзины в зависимости от находящихся в ней товаров.
// Товары в корзине хранятся в массиве. Задачи:
// a. Организовать такой массив для хранения товаров в корзине;
// b. Организовать функцию countBasketPrice, которая будет считать стоимость корзины.

console.log('\nЗадача 2');

let basket = [4.45, 2.00, 60.50, 33.18, 89.00]; // массив цен

function countBasketPrice(b) {              // b - входной массив
    let s = 0;                              // переменная накопительной суммы
    for (let i = 0; i < b.length; i++) {    // идём по массиву от нулевого элемента до конца масива
        s += b[i]                           // суммируем значения массива
    }
    return s                                // возвращаем сумму
}
console.log('Сумма:', countBasketPrice(basket));

