// Задача 1
// С помощью цикла while вывести все простые числа в промежутке от 0 до 100.
console.log('Задача 1');

const numbers = 100                     // диапазон поиска
let i = 0;                              // исследуемые числа
while (i <= numbers) {                  // перебираем числа в цикле 
    let j = 0, flag = true;             // нужен для прохождения самой первой итерации (по условию задачи стартуем именно от 0)
    while (i != j) {                    // перебираем делители от 0 ДО исследуемого (пока не сравняются)
        if (j < 2) { ++j; continue; }   // на 0 делить нельзя, а единица делитель без остатка для всех чисел, поэтому эти итерации пропускаем
        // эту проверку можно выкинуть если начинать счёт от 2х, но по условию от нуля..... Поэтому 0 - тоже простое ))))

        if (i % j == 0) { flag = false; break; } // если нашли дополнительный целочисленный делитель, прекращаем поиск и идём анализировать другое число
        ++j;
    }
    if (flag) console.log(i, 'Простое'); // если доп.делитель найден не был - печатаем число
    ++i;
}

